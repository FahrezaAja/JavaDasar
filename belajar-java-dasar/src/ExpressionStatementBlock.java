import java.util.Date;

public class ExpressionStatementBlock {
    public static void main(String[] args) { //ini adalah block, mulai dari { sampai }
        //block adalah sekumpulan statement

        //expression
        var value = 10; //ini adalah expression
        System.out.println(value = 100); //value = 100 adalah expression

        //statement
        //statement adalah kumpulan expression
        //statement adalah panjang baris dari awal sampai ;
        //jadi, var value = 10; juga termasuk statement

        //assignment statement
        double Avalue = 8933.234;
        //increment statement
        Avalue++;
        //method inovation statement
        System.out.println("Hello Wordl");
        //object creation statement
        Date date = new Date();

        
    }
}
